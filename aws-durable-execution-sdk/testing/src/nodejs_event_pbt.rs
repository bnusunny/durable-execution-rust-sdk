    /// Test that history does not use an "events" wrapper.
    #[test]
    fn test_no_events_wrapper() {
        let mut processor = EventProcessor::new();
        let operation = Operation::new("exec-1", OperationType::Execution);
        let update =
            create_operation_update("exec-1", OperationAction::Start, OperationType::Execution);

        let events = processor.process_operation_update(&update, &operation);
        let json_str = serde_json::to_string(&events).expect("Failed to serialize");

        // Should NOT contain "events" as a key
        assert!(
            !json_str.contains(r#""events""#),
            "History JSON should not contain 'events' wrapper key"
        );

        // Should be a direct array
        let json_value: Value = serde_json::from_str(&json_str).expect("Failed to parse");
        assert!(
            json_value.is_array(),
            "History should be a direct array, not wrapped in an object"
        );
    }
