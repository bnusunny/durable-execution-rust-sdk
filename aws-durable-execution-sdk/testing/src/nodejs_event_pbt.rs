    /// Strategy for generating valid (Action, Type) combinations that produce events.
    /// Only includes combinations that are mapped in the EventProcessor.
    fn valid_action_type_strategy() -> impl Strategy<Value = (OperationAction, OperationType)> {
        prop_oneof![
            // Execution events
            Just((OperationAction::Start, OperationType::Execution)),
            Just((OperationAction::Succeed, OperationType::Execution)),
            Just((OperationAction::Fail, OperationType::Execution)),
            // Step events
            Just((OperationAction::Start, OperationType::Step)),
            Just((OperationAction::Succeed, OperationType::Step)),
            Just((OperationAction::Fail, OperationType::Step)),
            Just((OperationAction::Retry, OperationType::Step)),
            // Wait events
            Just((OperationAction::Start, OperationType::Wait)),
            Just((OperationAction::Succeed, OperationType::Wait)),
            // Callback events
            Just((OperationAction::Start, OperationType::Callback)),
            // Context events
            Just((OperationAction::Start, OperationType::Context)),
            Just((OperationAction::Succeed, OperationType::Context)),
            Just((OperationAction::Fail, OperationType::Context)),
        ]
    }

    /// Strategy for generating a sequence of valid (Action, Type) combinations.
    fn action_type_sequence_strategy(
        min_len: usize,
        max_len: usize,
    ) -> impl Strategy<Value = Vec<(OperationAction, OperationType)>> {
        prop::collection::vec(valid_action_type_strategy(), min_len..=max_len)
    }

    // ============================================================================
    // Property 1: Event ID Monotonicity
    // **Validates: Correctness Property 1, Requirement 1.2**
    //
    // For any sequence of events E₁, E₂, ..., Eₙ generated by the EventProcessor:
    // - E₁.EventId = 1
    // - ∀i ∈ [2, n]: Eᵢ.EventId = Eᵢ₋₁.EventId + 1
    // ============================================================================

    proptest! {
        /// Property test verifying that EventIds are strictly increasing starting from 1.
        ///
        /// **Validates: Correctness Property 1, Requirement 1.2**
        ///
        /// This test generates random sequences of operations and verifies that:
        /// 1. The first event has EventId = 1
        /// 2. Each subsequent event has EventId = previous EventId + 1
        #[test]
        fn prop_event_id_monotonicity(
            sequence in action_type_sequence_strategy(1, 50)
        ) {
            let mut processor = EventProcessor::new();
            let mut all_events: Vec<NodeJsHistoryEvent> = Vec::new();

            // Generate events for each (action, type) combination
            for (i, (action, op_type)) in sequence.iter().enumerate() {
                let op_id = format!("op-{}", i);
                let operation = Operation::new(&op_id, *op_type);
                let update = create_operation_update(&op_id, *action, *op_type);

                let events = processor.process_operation_update(&update, &operation);
                all_events.extend(events);
            }

            // Verify the property: EventIds are strictly increasing starting from 1
            if !all_events.is_empty() {
                // First event must have EventId = 1
                prop_assert_eq!(
                    all_events[0].event_id, 1,
                    "First event must have EventId = 1, got {}",
                    all_events[0].event_id
                );

                // Each subsequent event must have EventId = previous + 1
                for i in 1..all_events.len() {
                    prop_assert_eq!(
                        all_events[i].event_id,
                        all_events[i - 1].event_id + 1,
                        "Event {} has EventId {}, expected {} (previous was {})",
                        i,
                        all_events[i].event_id,
                        all_events[i - 1].event_id + 1,
                        all_events[i - 1].event_id
                    );
                }
            }
        }
    }

    /// Helper function to create an OperationUpdate for testing.
    fn create_operation_update(
        op_id: &str,
        action: OperationAction,
        op_type: OperationType,
    ) -> OperationUpdate {
        match action {
            OperationAction::Start => {
                if op_type == OperationType::Wait {
                    OperationUpdate::start_wait(op_id, 30)
                } else {
                    let mut update = OperationUpdate::start(op_id, op_type);
                    update.result = Some("{}".to_string());
                    update
                }
            }
            OperationAction::Succeed => {
                OperationUpdate::succeed(op_id, op_type, Some("{}".to_string()))
            }
            OperationAction::Fail => OperationUpdate::fail(
                op_id,
                op_type,
                ErrorObject::new("TestError", "Test error message"),
            ),
            OperationAction::Retry => OperationUpdate::retry(op_id, op_type, None, Some(5)),
            OperationAction::Cancel => OperationUpdate::cancel(op_id, op_type),
        }
    }

    // ============================================================================
    // Property 2: Event Type Mapping Consistency
    // **Validates: Correctness Property 2, Requirements 2.1-2.12**
    //
    // For any operation update with action A and type T, the generated event type
    // must match the expected mapping from the design specification.
    // ============================================================================

    /// Returns the expected NodeJsEventType for a given (Action, Type) combination.
    /// Returns None if the combination doesn't produce an event.
    fn expected_event_type(
        action: OperationAction,
        op_type: OperationType,
    ) -> Option<NodeJsEventType> {
        match (action, op_type) {
            // Execution events
            (OperationAction::Start, OperationType::Execution) => {
                Some(NodeJsEventType::ExecutionStarted)
            }
            (OperationAction::Succeed, OperationType::Execution) => {
                Some(NodeJsEventType::ExecutionSucceeded)
            }
            (OperationAction::Fail, OperationType::Execution) => {
                Some(NodeJsEventType::ExecutionFailed)
            }
            // Step events
            (OperationAction::Start, OperationType::Step) => Some(NodeJsEventType::StepStarted),
            (OperationAction::Succeed, OperationType::Step) => Some(NodeJsEventType::StepSucceeded),
            (OperationAction::Fail, OperationType::Step) => Some(NodeJsEventType::StepFailed),
            (OperationAction::Retry, OperationType::Step) => Some(NodeJsEventType::StepFailed),
            // Wait events
            (OperationAction::Start, OperationType::Wait) => Some(NodeJsEventType::WaitStarted),
            (OperationAction::Succeed, OperationType::Wait) => Some(NodeJsEventType::WaitSucceeded),
            // Callback events
            (OperationAction::Start, OperationType::Callback) => {
                Some(NodeJsEventType::CallbackStarted)
            }
            // Context events
            (OperationAction::Start, OperationType::Context) => {
                Some(NodeJsEventType::ContextStarted)
            }
            (OperationAction::Succeed, OperationType::Context) => {
                Some(NodeJsEventType::ContextSucceeded)
            }
            (OperationAction::Fail, OperationType::Context) => Some(NodeJsEventType::ContextFailed),
            // Other combinations don't produce events
            _ => None,
        }
    }

    proptest! {
        /// Property test verifying that (Action, Type) → EventType mapping is correct.
        ///
        /// **Validates: Correctness Property 2, Requirements 2.1-2.12**
        ///
        /// This test generates all valid (Action, Type) combinations and verifies
        /// that the correct EventType is produced according to the mapping table.
        #[test]
        fn prop_event_type_mapping_consistency(
            (action, op_type) in valid_action_type_strategy()
        ) {
            let mut processor = EventProcessor::new();
            let operation = Operation::new("test-op", op_type);
            let update = create_operation_update("test-op", action, op_type);

            let events = processor.process_operation_update(&update, &operation);

            // Get the expected event type
            let expected = expected_event_type(action, op_type);

            match expected {
                Some(expected_type) => {
                    // Should produce exactly one event with the expected type
                    prop_assert_eq!(
                        events.len(), 1,
                        "Expected 1 event for ({:?}, {:?}), got {}",
                        action, op_type, events.len()
                    );
                    prop_assert_eq!(
                        events[0].event_type, expected_type,
                        "For ({:?}, {:?}): expected {:?}, got {:?}",
                        action, op_type, expected_type, events[0].event_type
                    );
                }
                None => {
                    // Should not produce any events
                    prop_assert!(
                        events.is_empty(),
                        "Expected no events for ({:?}, {:?}), got {}",
                        action, op_type, events.len()
                    );
                }
            }
        }
    }

