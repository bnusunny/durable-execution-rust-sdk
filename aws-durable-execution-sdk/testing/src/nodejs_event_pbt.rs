    // ============================================================================
    // Property 5: JSON Array Output Format
    // **Validates: Correctness Property 5, Requirements 5.1, 5.2, 5.3**
    //
    // For any serialized history H:
    // - H is a valid JSON array at root level (starts with `[`, ends with `]`)
    // - H does not contain an "events" wrapper key
    // - Events are ordered by EventId (strictly increasing)
    // ============================================================================

    proptest! {
        /// Property test verifying that history output is a flat JSON array without wrapper.
        ///
        /// **Validates: Correctness Property 5, Requirements 5.1, 5.2, 5.3**
        ///
        /// This test generates histories and verifies:
        /// 1. Output is a valid JSON array at root level
        /// 2. No "events" wrapper key exists
        /// 3. Events are ordered by EventId
        #[test]
        fn prop_json_array_output_format(
            sequence in action_type_sequence_strategy(1, 30)
        ) {
            let mut processor = EventProcessor::new();
            let mut all_events: Vec<NodeJsHistoryEvent> = Vec::new();

            // Generate events
            for (i, (action, op_type)) in sequence.iter().enumerate() {
                let op_id = format!("op-{}", i);
                let operation = Operation::new(&op_id, *op_type);
                let update = create_operation_update(&op_id, *action, *op_type);

                let events = processor.process_operation_update(&update, &operation);
                all_events.extend(events);
            }

            // Serialize as a flat JSON array (NodeJsHistoryFile format)
            let json_str = serde_json::to_string(&all_events)
                .expect("Failed to serialize history to JSON");

            // Property 1: Must be a valid JSON array at root level
            prop_assert!(
                json_str.starts_with('[') && json_str.ends_with(']'),
                "History JSON must start with '[' and end with ']', got: {}...{}",
                &json_str[..json_str.len().min(20)],
                &json_str[json_str.len().saturating_sub(20)..]
            );

            // Property 2: Must not contain "events" wrapper key
            let json_value: Value = serde_json::from_str(&json_str)
                .expect("Failed to parse JSON");

            prop_assert!(
                json_value.is_array(),
                "Root JSON value must be an array, got: {:?}",
                json_value
            );

            // Check that there's no "events" key anywhere at the root level
            if let Value::Object(map) = &json_value {
                prop_assert!(
                    !map.contains_key("events"),
                    "History JSON must not contain 'events' wrapper key"
                );
            }

            // Property 3: Events must be ordered by EventId (strictly increasing)
            if let Value::Array(arr) = &json_value {
                let mut prev_event_id: Option<u64> = None;
                for (i, event_value) in arr.iter().enumerate() {
                    if let Some(event_id) = event_value.get("EventId").and_then(|v| v.as_u64()) {
                        if let Some(prev_id) = prev_event_id {
                            prop_assert!(
                                event_id > prev_id,
                                "Events must be ordered by EventId. Event {} has EventId {}, but previous was {}",
                                i, event_id, prev_id
                            );
                        }
                        prev_event_id = Some(event_id);
                    }
                }
            }
        }
    }

